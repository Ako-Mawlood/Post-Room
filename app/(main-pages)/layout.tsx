import type { Metadata } from "next";
import "../globals.css";

export const metadata: Metadata = {
  title: "Welcome | Post-Room",
  description: "Generated by create next app",
};
import Search from "../components/pages/search/Search";
import Navbar from "../components/Navbar";
import axiosInstance from "@/libs/axiosInstance";
import { cookies } from "next/headers";
import { getCookie } from "cookies-next";
import { Button } from "../components/ui/button";

type userTagType = {
  id: number;
  name: string;
  createdAt: string;
};

const getUserTags = async () => {
  const token = getCookie("token", { cookies });
  try {
    const res = await axiosInstance.get("/api/category", {
      headers: { Authorization: token },
    });
    return res.data;
  } catch (err: any) {
    console.log(err.response.data);
  }
};
export default async function MainPagesLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const userTags: userTagType[] = await getUserTags();
  return (
    <>
      <Navbar />
      <main className="mx-6 flex w-full justify-center">
        {children}
        <aside>
          <Search />

          <div className="flex h-96 w-80 flex-col gap-4 rounded-2xl border px-3 pt-4">
            <h1 className="font-PT text-2xl font-semibold">Your tags</h1>
            <div className="flex w-full flex-wrap justify-center overflow-y-auto">
              {userTags?.map((tag) => {
                return (
                  <Button
                    key={tag.id}
                    variant="secondary"
                    size="sm"
                    className="m-1 h-8 text-center text-xs"
                  >
                    {tag.name}
                  </Button>
                );
              })}
            </div>
          </div>
        </aside>
      </main>
    </>
  );
}
